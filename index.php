<h3>Quem Somos</h3>
<p style="text-align: justify;">
  O Centro de Ensino de Itacoatiara - <b>CEI</b>, iniciou suas atividades em 2007, com a representatividade da <b>ULBRA</b> (<i>Universidade Luterana do Brasil</i>), até 2012, formando mais de trezentos alunos em Itacoatiara e municípios circunvizinho nos cursos: Bacharel em Ciências Sociais, Administração e Serviços Sociais; Licenciaturas em Pedagogia e Letras, Pós Graduação em Gestão e Coordenação Pedagógica.</br></br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Apartir de 2013, em parceria com outras <b>IES</b>, o <b>CEI</b> continuou oferecendo seus cursos, onde até a presente data, diplomou (cinco) turmas de "Pedagogia" e diversos alunos em especialização, nas áreas da Educação e Administração.</br></br>
   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Em 2017, o <b>CEI</b> firma uma nova franquia de serviços, com o "<b>GRUPO DIGAMMA/FACEL/EAD</b>", para melhor atender seus clientes e alunos com o oferecimento de cursos de graduação, pós graduação, pré-vestibular e capacitação, e cursos preparatórios, estendendo seu atendimento para a zona rural de Itacoatiara, e também para os municípios de Borba e Urucurituba.</br></br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Ao longo de dez anos o <b>CEI</b> tem preparado profissionais qualificados para o mercado de trabalho, contribuindo assim com o desenvolvimento sócio-educacional do nosso município.
